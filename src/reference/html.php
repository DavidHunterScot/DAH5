<p><b>Welcome to the HTML Reference!</b></p>
<p>Everything you need to know about HTML.</p>

<h2>What is HTML?</h2>
<p>HTML stands for HyperText Markup Language.</p>
<p>Think of it like a skeleton that gives your browser a basic layout of the page. In most cases, this is styled with CSS to look much better, as regular bare HTML can often not look as good when viewed as a web page in your browser.</p>

<h2>Structure</h2>
<p>Let's look at the HTML Structure of a basic web page.</p>

<code><pre>
&lt;!DOCTYPE html&gt;
&lt;html lang="en"&gt;
    &lt;head&gt;
        &lt;meta charset="UTF-8"&gt;
        &lt;meta name="viewport" content="width=device-width, initial-scale=1.0"&gt;

        &lt;title&gt;My First Web Page&lt;/title&gt;
    &lt;/head&gt;

    &lt;body&gt;

    &lt;/body&gt;
&lt;/html&gt;
</pre></code>
